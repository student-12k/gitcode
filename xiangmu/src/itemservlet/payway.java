	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		response.setContentType("text/html;charset=utf-8");
		PrintWriter out = response.getWriter();
		if (request.getSession().getAttribute("loginUser")==null){
			 response.sendRedirect("login.jsp"); 
			}
		String pay=(String) request.getSession().getAttribute("payway");
		urlDateBean data=new urlDateBean();
		if(pay.equals("支付宝")){
			 out.write("{\"payway\":1}");
		}else if(pay.equals("微信")){
			createCode bt=new createCode();
			String url="www.yij.com";
			try {
				String urlname=bt.createCodeByUrl(url);
				data.setPay("2");
				data.setUrlname(urlname);
				JSONObject jsonObjectol = JSONObject.fromObject(data);
			     out.write(jsonObjectol.toString());
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
	}

